import csv


def write_to_csv(X, Y, filename):
    assert len(X) == len(Y), "X and Y must have the same length"

    with open(filename, 'a', newline='') as csvfile:
        csvwriter = csv.writer(csvfile)

        # Write header row if needed
        csvwriter.writerow(['X', 'Y'])  # Optional: write header if X and Y need headers

        # Write rows
        for i in range(len(X)):
            csvwriter.writerow([X[i], Y[i]])



filename = 'data.csv'

write_to_csv(X, Y, filename)



